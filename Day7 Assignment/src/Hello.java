
public class Hello {
	void addition(int q, int e) {
		int c = q + e;
		System.out.println("Result: "+q+" + "+e+" = "+c);
	}
	void subtraction(int q, int e) {
		int m = q - e;
		System.out.println("Result: "+q+" - "+e+" = "+m);
		}
	void multiplication (int q, int e) {
		int n = q * e;
		System.out.println("Result: "+q+" x "+e+" = "+n);
	}
	void division (float q, float e) {
		float o = q / e;
		System.out.println("Result: "+q+" ÷ "+e+" = "+o);
	}
}
